     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validate($request,[
            'title'=>'required',
        ]);

        $service_areas = new ServiceArea;
        $service_areas->title = $request->input('title');
        $service_areas->save();

        return redirect('../public/servicearea/create')->with('success','Area Added');
    }


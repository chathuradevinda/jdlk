     */
    public function index()
    {
        $user_id  = Auth::id(); //get user id

        $feedback_details =  DB::table('feedback')->where('feedback.tradeperson_id',$user_id)
        ->join('users', 'feedback.customer_id', '=', 'users.id')
        ->select('feedback.*', 'users.first_name as fn','users.last_name as ln')
        ->get();

        return view('my_feedbacks.load_my_feedbacks',['feedback_details'=>$feedback_details]);

        
    }

